    public void cargarTabla() {
        limpiar(jTable1);
        mdb.cargarInformacion2(modelo, 3, "SELECT r.nombre,r.nombrecorto, r.domicilio\n"
                + "from usuarios u\n"
                + "inner join sociedadespersona s on (s.id_persona=u.idPersona)\n"
                + "inner join personam pm on (pm.ID=s.id_asociado)\n"
                + "inner join beneficioshumedos r on (r.idSociedad=pm.ID)\n"
                + "where u.usuario='" + usuario + "' and r.idEstado=1");
    }

    private void limpiar(JTable tabla) {
        while (tabla.getRowCount() > 0) {
            ((DefaultTableModel) tabla.getModel()).removeRow(0);
        }
    }

