    public void calcular() {

        try {
            limpiar(tablaDivisiones);
            DecimalFormat formato = new DecimalFormat("#.00");

            double kilosDisponibles = Double.parseDouble(lblKilosTotales.getText());
            int sacosDisponibles = Integer.parseInt(lblSacosTotales.getText());

            sacosDisponibles = sacosDisponibles / Integer.parseInt(jTextField1.getText());
            kilosDisponibles = kilosDisponibles / Double.parseDouble(jTextField1.getText());

            int valorCalculado = 0;

            if (!jTextField1.getText().equals(" ") || !jTextField1.getText().equals("0")) {
                int numDiv = Integer.parseInt(jTextField1.getText());
                for (int i = 0; i < numDiv; i++) {
                    modelo.addRow(new Object[]{i + 1, sacosDisponibles, formato.format(kilosDisponibles)});
                    valorCalculado = valorCalculado + Integer.parseInt(tablaDivisiones.getValueAt(i, 1) + "");
                    //sacos = sacos + Integer.parseInt(tablaDivisiones.getValueAt(i, 1) + "");
                    //kilos = kilos + Integer.parseInt(tablaDivisiones.getValueAt(i, 2) + "");
                }
            }

            valorCalculado = Integer.parseInt(lblSacosTotales.getText()) - valorCalculado;
            valorCalculado = valorCalculado + Integer.parseInt(modelo.getValueAt(0, 1) + "");
            modelo.setValueAt(valorCalculado, 0, 1);

            sumar();
            setBox(tablaDivisiones, tablaDivisiones.getColumnModel().getColumn(3));

        } catch (Exception e) {

        }

    }

    public void setBox(JTable tabla, TableColumn columna) {
        String[] datos = mdb.cargarCombos("select concat(responsable,', ',nombre,', ',placas) from vehiculo").split("Â¬");
        JComboBox c = new JComboBox((Object[]) datos);

        columna.setCellEditor(new DefaultCellEditor(c));
        DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
        columna.setCellRenderer(renderer);

    }

    public void sumar() {
        DecimalFormat formato = new DecimalFormat("#.00");
        int sacos = 0;
        double kilos = 0;

        for (int i = 0; i < modelo.getRowCount(); i++) {
            sacos = sacos + Integer.parseInt(modelo.getValueAt(i, 1) + "");
            kilos = kilos + Double.parseDouble(modelo.getValueAt(i, 2) + "");
        }

        jLabel6.setText(sacos + "");
        jLabel7.setText(formato.format(kilos) + "");

        if (kilos > Double.parseDouble(lblKilosTotales.getText())) {
            jLabel7.setForeground(Color.red);
        } else {
            jLabel7.setForeground(Color.BLACK);
        }

        if (sacos > Integer.parseInt(lblSacosTotales.getText())) {
            jLabel6.setForeground(Color.red);
        } else {
            jLabel6.setForeground(Color.BLACK);
        }
    }

    private void limpiar(JTable tabla) {
        while (tabla.getRowCount() > 0) {
            ((DefaultTableModel) tabla.getModel()).removeRow(0);
        }
    }

