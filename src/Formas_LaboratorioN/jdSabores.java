    public void intensidadSabor(JRadioButton radio) {
        String nuevoSabor = radio.getText();
        System.out.println("Nuevo "+nuevoSabor+"\nUltimo "+ultimoSabor);
        
        if (nuevoSabor.equals(ultimoSabor)) {
            cont++;
            System.out.println("En if cont = "+cont);
        } else {
            cont = 0;
            cont++;
            System.out.println("En else cont = "+cont);
        }

        if (cont == 4) {
            cont = 0;
        }
         System.out.println("En switch cont = "+cont);
        switch (cont) {
           
            case 1:

                radio.setBackground(Color.green);
                radio.setSelected(true);
                radio.setOpaque(true);
                break;
            case 2:

                radio.setBackground(Color.orange);
                radio.setSelected(true);
                radio.setOpaque(true);
                break;
            case 3:

                radio.setBackground(Color.red);
                radio.setSelected(true);
                radio.setOpaque(true);
                break;
            default:

                radio.setBackground(new Color(240, 240, 240));
                radio.setSelected(false);
                radio.setOpaque(false);
        }
        ultimoSabor = radio.getText();
    }

    public void crearCadenas() {
        String categoria = "";
        if (rbDry.isSelected()) {
            categoria = "Dry";
        } else if (rbWet.isSelected()) {
            categoria = "Wet";
        } else if (rbBreak.isSelected()) {
            categoria = "Break";
        } else if (rbFlavor.isSelected()) {
            categoria = "Flavor";
        } else if (rbAftertaste.isSelected()) {
            categoria = "Aftertaste";
        } else if (rbAcidity.isSelected()) {
            categoria = "Acidity";
        }
        switch (categoria) {
            case "Dry":
                if (Sweet3.isSelected()) {
                    cadenaDry += "Sweet";
                }
                if (BrownSugar3.isSelected()) {
                    cadenaDry += "BrownSugar";
                }
                if (Molasses3.isSelected()) {
                    cadenaDry += "Molasses";
                }
                if (MapleSugar3.isSelected()) {
                    cadenaDry += "MapleSugar";
                }
                break;
            case "Wet":
                if (Floral3.isSelected()) {
                    cadenaWet += "Floral";
                }
                if (Blacktea3.isSelected()) {
                    cadenaWet += "Blacktea";
                }
                if (Chamomile3.isSelected()) {
                    cadenaWet += "Chamomile";
                }
                if (Rose3.isSelected()) {
                    cadenaWet += "Rose";
                }
                break;
        }
        System.out.println(categoria);
    }

