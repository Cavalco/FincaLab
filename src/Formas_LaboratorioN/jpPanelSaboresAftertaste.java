    String cadena="";
    public void cadena(JTable tabla){
      
         for (int i=0; i<tabla.getRowCount(); i++ ) {
           if(tabla.getValueAt(i, 1) != null ){
             cadena += tabla.getValueAt(i,0)+"-"+tabla.getValueAt(i,1)+",";
         }
        }
         
        //jd.cadenas(cadena.substring(0,cadena.length()-1));
        jd.cadenas(cadena);
    }

